    /// ÁîüÊàêÂ≠óÁ¨¶ÂõæÁâáÂπ∂‰øùÂ≠òÂà∞ÊåáÂÆöË∑ØÂæÑ
    /// </summary>
    /// <param name="character">Ë¶ÅÁªòÂà∂ÁöÑÂ≠óÁ¨¶</param>
    /// <param name="savePath">‰øùÂ≠òË∑ØÂæÑ</param>
    /// <param name="fileName">Êñá‰ª∂ÂêçÂâçÁºÄ</param>
    /// <param name="imageSize">ÂõæÁâáÂ∞∫ÂØ∏</param>
